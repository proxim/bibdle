/** Generic fallback prompts for any character without curated questions. */
const FALLBACK_QUESTIONS = [
  "What moment from your life stays with you most?",
  "What was the hardest thing you faced?",
  "What did you learn about God?",
];

/** Returns curated conversation starters for a character, or sensible defaults. */
export function getCharacterQuestions(characterId: string): string[] {
  const curated = CHARACTER_QUESTIONS[characterId];
  return curated && curated.length > 0 ? curated : FALLBACK_QUESTIONS;
}
